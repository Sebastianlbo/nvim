local M = {}

local fn = vim.fn

local function git_branch()
  local cwd = vim.loop.cwd() or fn.getcwd()
  local cmd = "git -C " .. fn.shellescape(cwd) .. " rev-parse --abbrev-ref HEAD 2>/dev/null"
  local h = io.popen(cmd)
  if not h then
    return nil
  end
  local out = h:read("*a") or ""
  h:close()
  out = out:gsub("%s+$", "")
  if out == "" then
    return nil
  end
  return out
end

local function get_project_info()
  local width = 104
  local cwdname = fn.fnamemodify(vim.loop.cwd() or fn.getcwd(), ":t")
  local branch = git_branch() or "No Git Repo"
  local text = string.format("  %s    %s", cwdname, branch)

  local display = fn.strdisplaywidth(text)
  local pad = math.max(width - display, 0)
  local left = math.floor(pad / 2)
  local right = pad - left

  return string.rep(" ", left) .. text .. string.rep(" ", right)
end

